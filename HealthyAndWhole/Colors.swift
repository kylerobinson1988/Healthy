import UIKit

extension UIColor {
    
    class func mintGreen() -> UIColor {
        return UIColor(red:0.67, green:0.91, blue:0.83, alpha:1.00)
    }
    
    class func raspberryRed() -> UIColor {
        return UIColor(red:1.00, green:0.25, blue:0.42, alpha:1.00)
    }
    
}